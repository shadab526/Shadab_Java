public class Q003_SwapTowNumber {
    public static void main(String[] args) {
      //swap with use 3rd variable
        int a = 10, b= 20;
        int t;
        t=a;
        a=b;
        b=t;
        System.out.println("a:"+ a);
        System.out.println("b:"+b);
    }
}
